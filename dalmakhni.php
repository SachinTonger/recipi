    <p>This Dal Makhani recipe is a restaurant style version with subtle smoky flavors and creaminess of the lentils. If you love authentic Punjabi food then you are going to love this Dal Makhani even more.
    Dal Makhani is one of the most popular lentil recipes from the North Indian Punjabi cuisine made with Whole Black Lentils (known as Urad dal or Kaali Dal in Hindi) and Kidney Beans (known as Rajma in Hindi).

For this recipe, I have used a pressure cooker to cook the lentils, but I have explained the method of cooking the lentils in a pan or pot in the notes section of the recipe card.

I have also shared my expert tips and frequently asked questions by our readers on this recipe below. In the FAQ section below, I also share the method of making this dal makhani in the Instant Pot.
</p>
<h2> Why this recipe works</h2>
<p>Before I list out and explain to you the beauty of this recipe, let me tell you that this Dal Makhani recipe is one of our most reviewed and loved recipe on the blog.

It has been made by many of our readers and they have loved it. It also happens to be my most shared recipe on social media.

So what makes this recipe so good and why is it one of the best Dal Makhani recipe.</p>
<h1>How to make Dal Makhani</h1>
<h2>Soaking lentils</h2>
<ul>
  <li>1. Soak both ¾ cup whole urad dal (whole black gram) and ¼ cup rajma (kidney beans) overnight in enough water for 8 to 9 hours. Drain them well. The below photo shows the soaked whole urad dal and rajma.</li>
  <li>2. Rinse the urad lentils and rajma legumes a couple of times in water.</li>
  <li>3. Drain well and then add them in a 3 litre pressure cooker.</li>
  <li>4. Add 3 cups water and stir well.</li>
  <li>5. Pressure cook for 18 to 20 whistles on a high flame, till both the whole urad dal and rajma have cooked thoroughly and softened. If they are not cooked, then add about ½ cup water again and pressure cook for 4 to 5 whistles more. </li>
  <li>6. In the below photo, both the rajma and urad dal are cooked and softened well. The urad dal should melt in the mouth and should not give any bite or resistance when eaten.

You can also just mash the urad dal with a spoon or with your fingers to check the doneness. The same rule applies for kidney beans too.

Keep the cooked beans aside. Both the urad dal and kidney beans have to be fresh. If they are old or close to their expiry, they take a lot of time to cook.</li>
  <li>7. In a blender or mixer jar, take chopped tomatoes. 2 large tomatoes or 200 grams of tomatoes, chopped. No need to blanch the tomatoes.</li>
  <li>8. Blend to a smooth puree. Keep aside. You can also use the store brought 1 cup tomato puree instead of blending the tomatoes.</li>
  <li>9. In a pan, now heat 3 tablespoons butter. You can use salted butter or unsalted butter.</li>
  <li>10. Add the whole spices – ½ tsp cumin seeds, 2 to 3 cloves, 2 to 3 green cardamoms, 1 black cardamom, 1 inch cinnamon, 1 small to medium tej patta (Indian bay leaf). Fry till the spices become aromatic and sputter.</li>
  <li>11. Then add ½ cup finely chopped onions.</li>
  <li>12. Stir and saute the onions on a low flame often.</li>
  <li>13. Saute the onions till they become light golden.</li>
  <li>14. Then add 2 teaspoons ginger-garlic paste. Stir again and sauté till the raw aroma of ginger-garlic goes away.</li>
  <li>15. Add 1 teaspoon chopped green chilies and stir for a minute.</li>
  <li>16. Then add the prepared tomato puree.</li>
  <li>Add ½ teaspoon red chili powder.</li>
  <img src="image/chana.jfif" alt="Snow" width="276" height="200">
  <li>Then add about 2 to 3 pinches of grated nutmeg or nutmeg powder.</li>
  <li>20. Mix very well and sauté this mixture on a low to medium heat, till you see fat releasing from the sides. This takes about 3 to 4 minutes on a medium-low to medium heat.</li>
  <li>21. Then add the cooked urad dal and rajma beans.</li>
  <li>22. Add the remaining stock. Add 1 cup water or more if required.</li>
  <li>23. Mix very well and simmer the dal uncovered on a low heat.</li>
  <li>24. Keep on stirring often, so that the lentils don’t get stuck to the bottom of the pan. The lentils become viscous and start to stick at the bottom if not stirred. Mash a few lentils too while stirring.

</li>
  <li>25. Once the dal makhani has begun to thicken, add salt as required.</li>
  <li>26. Mix very well and continue to simmer on a low heat. Keep on stirring when the lentils are simmered on low heat. When simmering you can add more water if the consistency looks thick or dry.

The longer you keep dal makhni to simmer, the better it tastes. The lentils become creamy, viscous and the consistency of the dal will keep on thickening as you simmer.

I kept for about an overall 25 minutes on a low flame. Do keep on stirring at intervals.</li>
  <li>27. When the gravy has thickened enough, then add ¼ to ⅓ cup low fat cream or half and half. If using heavy cream, then add 2 tablespoons of it.

The consistency of Punjabi dal makhani is neither too thick or too thin. It has a medium consistency with a viscosity coming from the thoroughly cooked lentils.</li>
  <li>28. Mix the cream very well. Then switch off the heat.</li>
  <li>29. Now add ¼ teaspoon kasuri methi, crushed. Stir again. Cover and keep the dal makhani aside, if you are proceeding to the dhungar method. Or else you can serve Punjabi dal makhani straight away.</li>
  

</ul>

